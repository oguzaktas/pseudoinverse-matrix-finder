    private void text_rowKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_rowKeyPressed
        text_row.setEditable(true);
        int key = evt.getKeyCode();
        if ((key >= evt.VK_1 && key <= evt.VK_5) || (key >= evt.VK_NUMPAD1 && key <= evt.VK_NUMPAD5) || key == KeyEvent.VK_BACKSPACE || (key == KeyEvent.VK_ALT || key == KeyEvent.VK_TAB || key == KeyEvent.VK_ENTER || key == KeyEvent.VK_LEFT || key == KeyEvent.VK_RIGHT)) {
            text_row.setEditable(true);
            text_row.setBackground(Color.YELLOW);
        } else if ((key == evt.VK_0 || key == evt.VK_NUMPAD0)|| (key >= evt.VK_6 && key <= evt.VK_9) || (key >= evt.VK_NUMPAD6 && key <= evt.VK_NUMPAD9)) {
            text_row.setEditable(true);
            text_row.setBackground(Color.RED);
        } else {
            text_row.setEditable(false);
            text_row.setBackground(Color.RED);
        }
    }//GEN-LAST:event_text_rowKeyPressed

    private void text_columnKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_text_columnKeyPressed
        text_column.setEditable(true);
        int key = evt.getKeyCode();
        if ((key >= evt.VK_1 && key <= evt.VK_5) || (key >= evt.VK_NUMPAD1 && key <= evt.VK_NUMPAD5) || key == KeyEvent.VK_BACKSPACE || (key == KeyEvent.VK_ALT || key == KeyEvent.VK_TAB || key == KeyEvent.VK_LEFT || key == KeyEvent.VK_RIGHT)) {
            text_column.setEditable(true);
            text_column.setBackground(Color.YELLOW);
        } else if ((key == evt.VK_0 || key == evt.VK_NUMPAD0)|| (key >= evt.VK_6 && key <= evt.VK_9) || (key >= evt.VK_NUMPAD6 && key <= evt.VK_NUMPAD9)) {
            text_column.setEditable(true);
            text_column.setBackground(Color.RED);
        } else if (key == KeyEvent.VK_ENTER) {
            if (checkInputs()) {
                this.dispose();
                new CreateMatrixWindow(Integer.parseInt(text_row.getText()), Integer.parseInt(text_column.getText())).setVisible(true);
            }
        } else {
            text_column.setEditable(false);
            text_column.setBackground(Color.RED);
        }
    }//GEN-LAST:event_text_columnKeyPressed

