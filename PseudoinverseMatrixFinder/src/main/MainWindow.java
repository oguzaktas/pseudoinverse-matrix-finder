    public boolean checkInputs() {
        if (text_row.getText().trim().isEmpty() || text_column.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "<html><b>Bir ya da daha fazla alani bos biraktiniz.</b></html>");
            return false;
        } else if (Integer.parseInt(text_row.getText()) > 5 || Integer.parseInt(text_column.getText()) > 5 || Integer.parseInt(text_row.getText()) < 1 || Integer.parseInt(text_column.getText()) < 1) {
            JOptionPane.showMessageDialog(null, "<html><b>Matrisin satir ve sutun boyutlari 5'ten buyuk ve 1'den kucuk olamaz.</b></html>");
            return false;
        } else if (Integer.parseInt(text_row.getText()) == Integer.parseInt(text_column.getText())) {
            JOptionPane.showMessageDialog(null, "<html><b>Matrisin satir ve sutun degerleri esit (kare matris) olamaz.</b></html>");
            return false;
        } else {
            return true;
        }
    }

